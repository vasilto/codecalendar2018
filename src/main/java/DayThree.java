    static Integer extractStartingPointFromLeft(String plan) {
        return Integer.valueOf(plan.split("\\D+")[2]);
    }

    static Integer extractStartingPointFromTop(String plan) {
        return Integer.valueOf(plan.split("\\D+")[3]);
    }

    static Integer extractEndPointWide(String plan) {
        return Integer.valueOf(plan.split("\\D+")[4]);
    }

    static Integer extractEndPointTall(String plan) {
        return Integer.valueOf(plan.split("\\D+")[5]);
    }

    static String[][] applyElvPlan(ArrayList<String> plans, String[][] fabric) {
        int startingFromLeft;
        int startingPointFromTop;
        int endPointWide;
        int endPointTall;
        int numberOfOverlaps = 0;

        for (String plan :
                plans) {
            startingFromLeft = extractStartingPointFromLeft(plan);
            startingPointFromTop = extractStartingPointFromTop(plan);
            endPointTall = (extractEndPointTall(plan) + startingPointFromTop) - 1;
            endPointWide = (extractEndPointWide(plan) + startingFromLeft) - 1;
            for (int fromLeft = startingFromLeft; fromLeft <=endPointWide; fromLeft++) {
                for (int fromTop = startingPointFromTop; fromTop <=endPointTall; fromTop++) {
                    if (fabric[fromLeft][fromTop].equalsIgnoreCase("free")) {
                        fabric[fromLeft][fromTop] = "reserved";
                    } else {
                        if (fabric[fromLeft][fromTop].equalsIgnoreCase("reserved")) {
                            numberOfOverlaps ++;
                            fabric[fromLeft][fromTop] = "overlapped";
                        }
                    }
                }
            }
        }
        System.out.println("number of overlaps is:" + numberOfOverlaps);
        return fabric;
    }
